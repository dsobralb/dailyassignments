// 1) Subtract startDistance from endDistance and assign the result to distanceTraveled
let startDistance = 3;
let endDistance = 22;
let distanceTraveled = endDistance - startDistance; // Result: 19

// 2) Calculate the area of the yard
let yardWidth = 100;
let yardLength = 15;
let yardArea = yardWidth * yardLength; // Result: 1500 square feet

// 3) Calculate the average heart rate
let firstHeartRate = 60;
let secondHeartRate = 63;
let thirdHeartRate = 59;
let averageHeartRate = (firstHeartRate + secondHeartRate + thirdHeartRate) / 3; // Result: 60.67 BPM

// 4) Correct order of operations to get a result of 20
let result = 2 + (3 * 4); // Result: 20

// 5) Correct order of operations to get a result of 60
let result2 = (3 + 7) * -4 + 10; // Result: 60

// Console output to verify results
console.log(`distanceTraveled: ${distanceTraveled}`);
console.log(`yardArea: ${yardArea}`);
console.log(`averageHeartRate: ${averageHeartRate}`);
console.log(`result: ${result}`);
console.log(`result2: ${result2}`);
